#include <iostream>
using namespace std;
int main(){
    
    int a; //declaration
    a = 12; //initialization
    cout<<"size of int is "<<sizeof(a)<<endl;
    
    float b; //declaration
    cout<<"Size of float is "<<sizeof(b)<<endl;
    
    double c;
    cout<<"Size of double is "<<sizeof(c)<<endl;
    
    char d;
    cout<<"Size of Char is "<<sizeof(d)<<endl;
    
    bool e;
    cout<<"Size of boolean is "<<sizeof(e)<<endl;
    
    return 0;
}
